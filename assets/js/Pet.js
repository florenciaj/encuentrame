export class Pet {
    constructor(id, name, petType, breed, age, colour, gender, photo, state, features) {
        this.id = id;
        this.name = name;
        this.petType = petType;
        this.breed = breed;
        this.age = age;
        this.colour = colour;
        this.gender = gender;
        this.photo = photo;
        this.state = state;
        this.features = features;
    }
}